public class SumaDosNumeros {
    public static void main(String[] args) {
        int n1 = 10;
        int n2 = 4;
        // int suma = 0;
        int n3 = 11;
        int n4 = 13;

        //suma = n1 = n2;
        System.out.println(suma(n1,n2));

        //suma = n3 + n4;
        System.out.println(suma(n3,n4));

    
    }

    public static int suma(int n1, int n2) {
        return n1+n2;
    }

}
